<script>
    document.addEventListener("DOMContentLoaded", () => {
        const items = document.querySelectorAll('.polaroid-item');
        let activeIndex = 0; // Penanda gambar mana yang sedang di depan

        // Definisi 3 Posisi (Menggunakan string class Tailwind)
        // Kita tambahkan scale dan opacity untuk efek kedalaman 3D
        const positions = [
            // Posisi 1: Depan (Front) - Paling besar, jelas, di atas
            "z-30 scale-105 opacity-100 -rotate-3 -translate-x-8 translate-y-12",
            
            // Posisi 2: Tengah (Middle) - Sedikit lebih kecil, agak transparan
            "z-20 scale-95 opacity-80 rotate-6 translate-x-8 translate-y-4",
            
            // Posisi 3: Belakang (Back) - Paling kecil, paling transparan
            "z-10 scale-90 opacity-60 -rotate-6 translate-y-0"
        ];

        function rotateImages() {
            items.forEach((item, index) => {
                // Hitung posisi relatif berdasarkan activeIndex
                // Modulo (%) memastikan urutan berputar 0 -> 1 -> 2 -> 0 dst.
                let positionIndex = (index - activeIndex + items.length) % items.length;
                
                // 1. Hapus kelas posisi Tailwind sebelumnya (menggunakan Regex agar bersih)
                item.className = item.className.replace(/\b(z-\d+|scale-\d+|opacity-\d+|rotate-[\d-]+|translate-[xy]-[\d-]+)\b/g, '');
                
                // 2. Tambahkan kelas posisi yang baru
                item.className += ` ${positions[positionIndex]}`;
            });

            // Geser penanda ke gambar berikutnya
            activeIndex = (activeIndex + 1) % items.length;
        }

        // Jalankan sekali di awal agar posisi langsung terbentuk
        rotateImages();

        // Set interval untuk menjalankan fungsi setiap 2000ms (2 detik)
        setInterval(rotateImages, 2000);
    });
</script>
